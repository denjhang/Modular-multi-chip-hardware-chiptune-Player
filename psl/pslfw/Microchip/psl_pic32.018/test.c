							WriteOpxPcmd8Fpga(cs, 0, 0x0010+baddr, 0x00, 0);
							WriteOpxPcmd8Fpga(cs, 0, 0x0020+baddr, 0x00, 0);
							WriteOpxPcmd8Fpga(cs, 0, 0x0030+baddr, 0x01, 0);
							WriteOpxPcmd8Fpga(cs, 0, 0x0050+baddr, 0x1f, 0);
