int str2dec(char *s, int base, int *v, char **q)
{
	int f;

	//
	f = 1;
	*v = 0;
	if(q!=NULL)
		*q = NULL;
	while(f){
		switch(*s){
			case '0':	case '1':	case '2':	case '3':	case '4':
			case '5':	case '6':	case '7':	case '8':	case '9':
				*v = (*v)*base+((*s)-'0');
				s++;
				break;
			case 'a':	case 'b':	case 'c':	case 'd':	case 'e':	case 'f':
			case 'A':	case 'B':	case 'C':	case 'D':	case 'E':	case 'F':
				if(base<10){
					*v = 0;
					return 1;
				}
				*v = (*v)*base+(((*s)|0x20)-'a')+0x0a;
				s++;
				break;
			case '\t':	case ' ':
				s++;
				break;
			case ',':
				f = 0;
				break;
			case '\0':	case '\n':	case '\r':
				f = 0;
				break;
			default:
				*v = 0;
				return 1;
		}
	}
	if(q!=NULL)
		*q = s;

	return 0;
}

int hex(char *s, unsigned char inst[8], int lim)
{
	int f, n, v;
	char *p;

	//
	f = 1;
	n = 0;
	while(f){
		switch(*s){
			case ',':
				n++;
				break;
			case '\t':	case ' ':
				break;
			case '\0':	case '\n':	case '\r':
				f = 0;
				break;
			default:
				if(n>=lim){
					f = 0;
					break;
				}
				//
				if(*s=='$'){
					if(str2dec(s+1, 16, &v, &p))
						return 1;
				} else
				if(*s=='0' && (*(s+1)|0x20)=='x'){
					if(str2dec(s+2, 16, &v, &p))
						return 1;
				} else
				if(*s>='0' && *s<='9'){
					if(str2dec(s, 10, &v, &p))
						return 1;
				} else
					return 1;
				//
				if(p==NULL || v<0x00 || v>0xff)
					return 1;
				inst[n] = v;
				s = p-1;
				break;
		}
		s++;
	}

	return 0;
}

int ini(char *path, unsigned char inst[16+3][8])
{
	s32 fh, fs;
	int f;
	char *p, *q, *r, buf[128];

	//
	printf(".ini(%s)\n", path);
	fh = PcOpen(path, PC_RDONLY|PC_BINARY);
	if(fh==-1){
		printf(".PcOpen(%s)\n", path);
		return 1;
	}
	fs = PcLseek(fh, 0, PC_SEEK_END);
	PcLseek(fh, 0, PC_SEEK_SET);

	//
	while(1){
		//
		if(PcFgets(buf, sizeof(buf), fh, &fs)==NULL)
			break;
		//
		f = 1;
		p = buf;
		q = r = NULL;
		while(f){
			switch(*p){
				case '=':
					if(q==NULL)
						f = 0;
					else
					if(r==NULL){
						*p = '\0';
						r = p+1;
					}
					break;
				case '#':
					*p = '\0';
					f = 0;
					break;
				case '\t':	case ' ':
					break;
				case '\0':	case '\n':	case '\r':
					*p = '\0';
					f = 0;
					break;
				case '[':	case ']':
					f = 0;
					break;
				default:
					if(q==NULL)
						q = p;
					break;
			}
			p++;
		}
		//
		if(q!=NULL && r!=NULL){
//			printf("%d,[%s],[%s]\n", fs, q, r);
			int instn;
			if(str2dec(q, 10, &instn, NULL)==0){
				if(instn>=0 && instn<=18){
//					printf("@%d,%d", sizeof(inst[instn]), sizeof(inst[instn])/sizeof(inst[instn][0]));
					if(hex(r, inst[instn], sizeof(inst[instn])/sizeof(inst[instn][0])))
						memset(inst[instn], 0x00, sizeof(inst[instn]));
				}
			}
		}
	}

	//
	PcClose(fh);
	return 0;
}

