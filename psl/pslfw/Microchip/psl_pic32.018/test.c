				break;

			case 0x31:	//1
			case 0x32:	//2
			case 0x33:	//3
			case 0x34:	//4
			case 0x35:	//5
			case 0x36:	//6
			case 0x37:	//7
			case 0x38:	//8
			case 0x39:	//9
